				<section id="avenuesforpublicity">
					<h2>Avenues For Publicity</h2>
					<h4 id="tldr">tl;dr</h4>
					<img src="http://i.imgur.com/KzqvRqq.png" style="width:66%;" alt="tldrlogo">
					<p>Our weekly email newsletter, “tl;dr”, summarizes the events happening on campus in the current week. It also publicizes upcoming events (both internal and external) and new opportunities (volunteering, scholarship, competitions etc.). “tl;dr” stands for “Too Long; Didn’t Read”, and also “The Latest Daily Roundup”.</p>

					<p>It is sent out every Sunday night, to all students and staff of SUTD. The content that we include in the newsletter are publicity requests by both students and staff. If you wish to have your item included in our newsletter, please email communications@studentgov.sutd.edu.sg by the previous night (Saturday 11:59pm) with the following:</p>

					<p><ul>
						<li>A poster of any dimension containing every detail about your event. Refer to Posters segment for more details</li>
						<li>A one-sentence summary of your event/item to capture attention</li>
						<li>Relevant links (if any)</li>
					</ul></p>

					<p>
						We will only accept requests that have these requirements fulfilled, and reserve the right to decline any publicity request if we feel the content goes against any of our guidelines.
					</p>

					<p>
						Please note that each event will only appear in “tl;dr” at most twice – once under “New Event Broadcasts” in the subsequent week’s publication following your publicity request, and once under “Events of the Week” when your event’s date arrives. This is to keep “tl;dr” short and sweet, so it remains readable for all. But do not worry, we compound all events and opportunities on the Root website. 
					</p>

					<h4 id="emails">Emails</h4>
					<p>The following will apply for mass emails to the student population, from both clubs and individuals.</p>

					<h5>Eligibility</h5>
					<p>For established clubs, only club emails can be used for event publicity, while only one person from probationary clubs can use his/her school email account to send emails for each event. Have a consistent sender so people can identify the sender with the club/organization.</p>
					<p>If you are involved in a multi-club event, ONLY the main organizing club is allowed to send emails. 10 different clubs sending 20 emails about their booth on the same day is NOT cool.</p>

					<h5>Frequency</h5>
					<p>You SHALL NOT send more than TWO emails for the same event or sub-event (if it is part of a bigger event). You are recommended to send one email at least one week before the actual event and then a final call or reminder email a day or two before the event.</p>
					<p>It’s both to help you start informing people early and reduce spam.</p>

					<h5>Size</h5>
					<p>
						500 KB (kilobytes) is the limit for mass emails, to avoid bloat and crazy data traffic for mobile users. You shouldn’t need pictures larger than 1200px and videos should not be sent via email at all. If you’re really desperate, consider sending a link instead.
					</p>

					<h5>Addressing</h5>
					<p>Ensure you use the BCC to email, so as to avoid people accidentally hitting “Reply All” and spamming the whole school, which has happened before.</p>
					<p>This is where you can find the BCC option in the Outlook web browser:
						<img src="http://i.imgur.com/WA9RPys.png" alt="where_to_bcc"></p>


					<h4 id="facebook">Facebook</h4>
					<p>
						You can advertise on the SUTD Facebook groups after your event is approved by the Events committee (events@studentgov.sutd.edu.sg) and your poster approved by the Communications committee, but post no more than twice about the same event. If it’s a multi-club event, each club should post no more than once. We may delete extra posts without notice.
					</p>
					<p>
						Do approach any friendly Root member if you would like your post pinned i.e. kept at the top of the group’s newsfeed. However, do note that we will only be able to pin the post for a maximum period of one week, subject to our discretion.
					</p>
					<p>
						Posters are still subject to guidelines at the Posters segment.
					</p>

					<h4 id="posters">Posters</h4>
					<h5>Content</h5>
					<p>You should include all relevant information regarding the event, such as date, time, venue and event description.</p>
					<p>Poster is to explicitly state the person publishing the material, and include his/her contact details, so that Office of Campus Development staff can contact the person-in-charge in case of issues. (Note: This is only necessary for posters that will be printed and put up around campus. Posters that will only be circulated via online channels do not need to fulfill this requirement.)</p>
					<p>Posters should NOT display content that directly promotes political and religious causes.</p>
					<p>It is recommended that you leave a 3cm x 3cm light-colored space on your poster for us to stamp.</p>
					<h5>Procedure</h5>
					<img src="http://i.imgur.com/gdMd16X.png" alt="posterprocedure">
					<h5>Permissible Locations, Maximum Size Allowed & Maximum Quantity</h5>
					<p>Posters can only be displayed at the locations listed within the Display Venues Form.
					</p>
					<p>
						The maximum size of the poster allowed for each location is specified beside its corresponding table. You can only put one poster at each location. Each student organization starts with a quota of 20 posters per event.
					</p>

					<h5>Period of Display</h5>
					<p>
						Posters may be put up to 4 weeks before the date of the event. They are to be removed by the event organizers once the event is over. Office of Campus Development staff will remove those that are locked within the noticeboard panels. For posters that do not specify an end date, they will be removed after 1 week of display.
					</p>
					<p>
						For posters publicizing events that take place over a long period of time (i.e. approximately 6 months), they may be removed before the closing date of the event to make way for newer items if necessary. Office of Campus Development staff will assess each poster of this nature on a case-by-case basis.
					</p>
					<p>
						We reserve the right to remove any unapproved or unremoved posters. The number of violations will be deducted from your student organization’s quota. The quota will only be reset on January during pitching.
					</p>
					<h5>Hostel</h5>
					<p>
						If you want to put up your posters at the hostel noticeboards, there is no need to gain approval from Root Communications and the Office of Campus Development. Send your request to EMS directly.
					</p>

